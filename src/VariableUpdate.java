public class VariableUpdate{
    public static void main(String[] args) {
        int marksJose = 0,  marksJuan = 0;

        System.out.println(marksJose);
        System.out.println(marksJuan);

        marksJose = marksJose + 50;
        marksJuan = 80;

        System.out.println(marksJose);
        System.out.println(marksJuan);

    }

}
